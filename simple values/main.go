package main

import "fmt"

func main() {
	// Strings
	fmt.Println("Simple values in golang.")

	// Integers
	fmt.Println(4 + 5)

	// Boolean values
	fmt.Println(true)

	// Float Values
	fmt.Println(56.78)

	// Mathematical Calculations
	fmt.Println(78 / 7)
	fmt.Println(78 * 7)
	fmt.Println(78 + 7)
	fmt.Println(78 - 7)
}
